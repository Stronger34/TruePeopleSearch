def random_user_agent():
    return random.choice(user_agent_list)


def scrape(url):
    if not url:
        return

    response = requests.get(url, headers={"User-Agent": random_user_agent()})

    if "InternalCaptcha" in response.url:
        webbrowser.open_new_tab(response.url)
        raise MaltegoException("Internal Captcha")

    if response.status_code == requests.codes.ok:
        return BeautifulSoup(response.content, "html.parser")
    raise MaltegoException(response.status_code)
